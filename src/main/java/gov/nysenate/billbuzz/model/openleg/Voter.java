package gov.nysenate.billbuzz.model.openleg;

public class Voter
{
    private String name;
    private String vote;

    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }

    public String getVote()
    {
        return vote;
    }
    public void setVote(String vote)
    {
        this.vote = vote;
    }
}
